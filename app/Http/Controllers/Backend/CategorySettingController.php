     * Show the form for editing the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function edit($id)
    {
        $category = Category::findOrFail($id);
        $attributesGroup = AttributeGroup::all();
        return view('admin.categorySettings.edit', compact(['category', 'attributesGroup']));
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $category = Category::findOrFail($id);
        $category->attributesGroup()->sync($request->attributesGroup);
        $category->save();
        \Illuminate\Support\Facades\Session::flash('created', "ویژگی ها با موفقیت به دسته بندی $category->name اضافه شد.");
        return redirect(route('categories.index'));
    }

